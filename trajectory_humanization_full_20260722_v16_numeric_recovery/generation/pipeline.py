def _atomic_json(path: Path, data: Dict[str, object]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    temporary = path.with_name(path.name + ".tmp.%d.%s" % (os.getpid(), uuid.uuid4().hex))
    try:
        with temporary.open("w", encoding="utf-8") as stream:
            json.dump(data, stream, indent=2, sort_keys=True)
            stream.write("\n")
            stream.flush()
            os.fsync(stream.fileno())
        os.replace(str(temporary), str(path))
        directory_fd = os.open(str(path.parent), os.O_RDONLY | os.O_DIRECTORY)
        try:
            os.fsync(directory_fd)
        finally:
            os.close(directory_fd)
    finally:
        if temporary.exists():
            temporary.unlink()


def unit_output_path(output_root: str, unit: GenerationUnit) -> Path:
    return (
        Path(output_root) / "shards" /
        ("shard_%03d_of_%03d" % (unit.shard_id, unit.num_shards)) /
        unit.action / ("user_%03d.npz" % unit.user_id)
    )


def generate_unit(
    unit: GenerationUnit,
    real_pool: Sequence[CanonicalTrajectory],
    fixed_split: FixedUserSplit,
    reference_registry: ReferenceRegistry,
    train_prior: TrainGlobalPrior,
    model: TrajectoryDiffusion,
    checkpoint_sha256: str,
    output_root: str,
    base_seed: int,
    batch_size: int,
    device: torch.device,
    resume: bool = True,
    max_aggregate_clip_rate: float = 0.05,
    max_event_clip_rate: float = 0.25,
) -> Dict[str, object]:
    if batch_size <= 0 or model.action != unit.action or train_prior.action != unit.action:
        raise ValueError("unit/model/prior/batch configuration mismatch")
    runtime_determinism = require_strict_runtime_determinism()
    runtime_digest = runtime_determinism_sha256(runtime_determinism)
    target = unit_output_path(output_root, unit)
    if target.exists():
        if not resume:
            raise FileExistsError(str(target))
        if validate_existing_unit(
            str(target), ACTION_TO_ID[unit.action], unit.user_id, unit.samples, 50,
            generation_base_seed=base_seed,
            generation_batch_size=batch_size,
            runtime_determinism_sha256=runtime_digest,
            training_diffusion_steps=model.diffusion_steps,
        ):
            with np.load(str(target), allow_pickle=False) as existing:
                existing_checkpoint = bytes(np.asarray(existing["checkpoint_sha256"], np.uint8).tolist()).hex()
            if existing_checkpoint != checkpoint_sha256:
                raise ValueError("existing resume unit was generated by a different checkpoint")
            # Resume is a trust boundary: run the same complete provenance,
            # replay, geometry and lifecycle audit used before publication.
            audit = audit_generated_unit(
                str(target), real_pool, fixed_split, reference_registry, train_prior,
                expected_count=unit.samples, expected_base_seed=base_seed,
                expected_generation_batch_size=batch_size,
                expected_ddim_steps=50,
                max_aggregate_clip_rate=max_aggregate_clip_rate,
                max_event_clip_rate=max_event_clip_rate,
                max_alpha_bar_final=1.0 if model.diffusion_steps < 1000 else 0.001,
            )
            # Keep resumed and newly generated unit reports on the same
            # provenance contract.  A shard may be skipped only when the
            # supervisor can still prove that it is neural DDIM output,
            # without a selector, under the current formal batch policy.
            audit.update(
                status="resumed",
                path=str(target.resolve()),
                output_path=str(target.resolve()),
                n_fake=unit.samples,
                batch_size=batch_size,
                neural_ddim=True,
                selector_used=False,
            )
            _atomic_json(target.with_suffix(".audit.json"), audit)
            return audit

    refs = reference_registry.resolve(real_pool, unit.action, unit.user_id, unit.split)
    reference_sets = [refs] * unit.samples
    policy = ReferenceConditionPolicy(train_prior)
    requests = [
        policy.sample(unit.action, unit.user_id, unit.split, sample_index, base_seed, refs)
        for sample_index in range(unit.samples)
    ]
    records: List[AndroidTrajectoryRecord] = []
    used_noise_seeds: List[int] = []
    model.eval()
    started = time.time()
    for batch_start in range(0, unit.samples, batch_size):
        batch_end = min(unit.samples, batch_start + batch_size)
        request_batch = requests[batch_start:batch_end]
        refs_batch = reference_sets[batch_start:batch_end]
        batch = build_sampling_batch(request_batch, refs_batch, device)
        noise_seeds = [
            ddim_noise_seed(x.seed, x.action, x.user_id, x.sample_index)
            for x in request_batch
        ]
        used_noise_seeds.extend(noise_seeds)
        output = sample_ddim_seeded_batch(model, batch, noise_seeds, inference_steps=50)
        records.extend(record_from_generated(output, batch, i, request_batch[i]) for i in range(len(request_batch)))
    if len(records) != unit.samples:
        raise AssertionError("generation unit returned wrong count")
    arrays = build_numeric_archive(
        records, ddim_steps=50, eta=0.0,
        training_diffusion_steps=model.diffusion_steps,
        alpha_bar_final=float(model.alpha_bar[-1].detach().cpu().item()),
        checkpoint_sha256=checkpoint_sha256,
        split_sha256=fixed_split.source_sha256,
        reference_registry_sha256=reference_registry.registry_sha256,
        runtime_determinism_sha256=runtime_digest,
        generation_base_seed=base_seed,
        generation_batch_size=batch_size,
        ddim_noise_seeds=used_noise_seeds,
    )

    # Audit a private staging file, then atomically publish.  A failed unit is
    # never mistaken for a completed resume artifact.
    staging = target.with_name(target.name + ".staging.%d.%s" % (os.getpid(), uuid.uuid4().hex))
    try:
        atomic_save_npz(str(staging), arrays)
        audit = audit_generated_unit(
            str(staging), real_pool, fixed_split, reference_registry, train_prior,
            expected_count=unit.samples, expected_base_seed=base_seed,
            expected_generation_batch_size=batch_size,
            expected_ddim_steps=50,
            max_aggregate_clip_rate=max_aggregate_clip_rate,
            max_event_clip_rate=max_event_clip_rate,
            max_alpha_bar_final=1.0 if model.diffusion_steps < 1000 else 0.001,
        )
        target.parent.mkdir(parents=True, exist_ok=True)
        if target.exists():
            raise FileExistsError(str(target))
        os.replace(str(staging), str(target))
        directory_fd = os.open(str(target.parent), os.O_RDONLY | os.O_DIRECTORY)
        try:
            os.fsync(directory_fd)
        finally:
            os.close(directory_fd)
    finally:
        if staging.exists():
            staging.unlink()
    audit.update(
        status="generated", path=str(target.resolve()), output_path=str(target.resolve()),
        elapsed_seconds=time.time() - started,
        batch_size=batch_size, neural_ddim=True, selector_used=False,
    )
    _atomic_json(target.with_suffix(".audit.json"), audit)
    return audit
